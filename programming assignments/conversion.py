"""
This program contains
two functions that convert
celsius to fahrenheit and
vice versa
"""


# returns celsius value
def c2f(val1):
    celsius = val1 * 1.8 + 32
    return celsius


# returns fahrenheit value
def f2c(val2):
    fahren = (val2 - 32) / 1.8
    return fahren
